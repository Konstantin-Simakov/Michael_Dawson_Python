# Demonstrates mathematic operations

print("If a pregnant female hippo weighing 800 kg gives birth to a calf weighing 40 kg,")
print("but then eats 20 kg of food, how much will she weigh after all this?")
input("Press the key <Enter> to find out.")
print("800 - 40 + 20 =", 800 - 40 + 20)

print("\nIf a student, returning from a successfully passed exam, joyfully")
print("bought each of 6 friends 3 large sweets, then how many large sweets were bought in total?")
input("Press the key <Enter> to find out.")
print("6 * 3 =", 6 * 3)

print("\nIf at a restaurant you and your friends were brought a bill for $19 (including tips),")
print("and you decided to chip in equally for four, then how much would you get from each?")
input("Press the key <Enter> to find out.")
print("19 / 4 =", 19 / 4)

print("\nIf four pirates find a chest containing exactly 107 gold coins and")
print("decide to divide the booty equally, how many whole coins will each get?")
input("Press the key <Enter> to find out.")
print("107 // 4 =", 107 // 4)

print("\nIf the same four pirates share the same 107 coins from the casket equally,")
print("how many coins will remain unclaimed?")
input("Press the key <Enter> to find out.")
print("107 % 4 =", 107 % 4)

